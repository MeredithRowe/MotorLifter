void ButtonDownProc2(void)
{
    int32_t xPosValue = 0,yPosValue = 0;

    BSP_LED_On(LED2);

		if(gMotionDirection){
//			BSP_MotorControl_Run(MOTOR_LIFTER_X_AXIS_DEVICE_ID, FORWARD);
            BSP_MotorControl_Move(MOTOR_LIFTER_Y_AXIS_DEVICE_ID, FORWARD, 800);
            BSP_MotorControl_Move(MOTOR_LIFTER_X_AXIS_DEVICE_ID, FORWARD, 3200);
			gMotionDirection = 0;
		}else{
//			BSP_MotorControl_Run(MOTOR_LIFTER_X_AXIS_DEVICE_ID, BACKWARD );
            BSP_MotorControl_Move(MOTOR_LIFTER_Y_AXIS_DEVICE_ID, BACKWARD, 800);
            BSP_MotorControl_Move(MOTOR_LIFTER_X_AXIS_DEVICE_ID, BACKWARD, 3200);
			gMotionDirection = 1;
		}
        
        /* Wait for device ends moving */
        BSP_MotorControl_WaitWhileActive(MOTOR_LIFTER_X_AXIS_DEVICE_ID);
//        BSP_MotorControl_WaitForAllDevicesNotBusy();
        
        xPosValue = BSP_MotorControl_GetPosition(MOTOR_LIFTER_X_AXIS_DEVICE_ID);
        yPosValue = BSP_MotorControl_GetPosition(MOTOR_LIFTER_Y_AXIS_DEVICE_ID);
        printf("Dir=%d,Xpos=%d,Ypos=%d\r\n", gMotionDirection, xPosValue, yPosValue);

		gKeyDown_once = 0;
}

static void ButtonDownProc(void)
{
//    BSP_MotorControl_Run(MOTOR_LIFTER_X_AXIS_DEVICE_ID, FORWARD);
//    BSP_MotorControl_Run(MOTOR_LIFTER_Y_AXIS_DEVICE_ID, FORWARD);
    if(gMotionDirection){
        BSP_MotorControl_Run(MOTOR_LIFTER_Z_AXIS_DEVICE_ID, FORWARD);
        gMotionDirection = BACKWARD;
    }else{
        BSP_MotorControl_Run(MOTOR_LIFTER_Z_AXIS_DEVICE_ID, BACKWARD);
        gMotionDirection = FORWARD;
    }
}

void ButtonHandler(void)
{
	if(gKeyDown_once){
        ButtonDownProc();
        gKeyDown_once = 0;
	}else{
		BSP_MotorControl_SoftStop(MOTOR_LIFTER_Z_AXIS_DEVICE_ID);
		gKeyDown_once = 1;
	}
}


